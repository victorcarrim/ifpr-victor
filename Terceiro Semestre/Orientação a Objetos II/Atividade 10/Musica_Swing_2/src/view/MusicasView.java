        artistasField.setText(musicasTable.getValueAt(musicasTable.getSelectedRow(), MusicasTableModel.COL_ARTISTAS_MUSICA).toString());
        gravadoraField.setText(musicasTable.getValueAt(musicasTable.getSelectedRow(), MusicasTableModel.COL_GRAVADORA_MUSICA).toString());
