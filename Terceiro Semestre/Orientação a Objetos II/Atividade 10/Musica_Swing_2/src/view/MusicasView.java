    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(musicaField.getText().equals("") || artistasField.getText().equals("") || gravadoraField.getText().equals("") || tempoField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
        
            musicasObject = new Musica();
            musicasObject.setNomeMusica(musicaField.getText());
            musicasObject.setArtistasMusica(artistasField.getText());
            musicasObject.setGravadoraMusica(gravadoraField.getText());
            musicasObject.setTempoMusica(tempoField.getText());
            musicasObject.setDataProducaoMusica(dataProducaoField.getText());

            
            //System.out.println(musicasObject.getNomeMusica() + musicasObject.getArtistasMusica() + musicasObject.getGravadoraMusica() + musicasObject.getTempoMusica() + musicasObject.getDataProducaoMusica());
            
            try {
                if(codigoField.getText().equals("")){
                daoGenericoObject.inserir(musicasObject);
            } else {
                musicasObject.setIdMusica(Integer.parseInt(codigoField.getText()));
                daoGenericoObject.alterar(musicasObject, "idMusica", musicasObject.getIdMusica());
            }
            } catch (Exception e) {
                System.out.println(e);
            }
            
            
            
            this.limparCampos();
            this.carregaTable();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

