    public void limparCampos(){
        musicaField.setText("");
        artistasField.setText("");
        gravadoraField.setText("");
        tempoField.setText("");
        dataProducaoField.setText("");
        codigoField.setText("");
}
    public void carregaTable(){
        try {
            musicasTable.setModel(new MusicasTableModel(daoGenericoObject.buscarTodos(new Musica())));
            btnExcluir.setEnabled(false);
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }
