    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int confirma = JOptionPane.showConfirmDialog(null, "Confirmar Exclusão?", "Exclusão de Veículo", JOptionPane.YES_NO_OPTION);
        if(confirma == 0){
            try {
                daoGenericoObject.excluir(new Musica(), "idMusica", Integer.parseInt(codigoField.getText()));
            } catch (Exception e) {
                System.out.println(e);
            }
            
            this.limparCampos();
            this.carregaTable();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

