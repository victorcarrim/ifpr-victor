    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(marcaField.getText().equals("") || modeloField.getText().equals("") || chassiField.getText().equals("") || anoField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos", "Aviso", JOptionPane.WARNING_MESSAGE);
        } else {
        
            veiculoObject = new Veiculo();
            veiculoObject.setMarca(marcaField.getText());
            veiculoObject.setModelo(modeloField.getText());
            veiculoObject.setChassi(chassiField.getText());
            veiculoObject.setAno(Integer.parseInt(anoField.getText()));
            if(codigoField.getText().equals("")){
                daoVeiculoObject.inserir(veiculoObject);
            } else {
                veiculoObject.setCodigo(Integer.parseInt(codigoField.getText()));
                daoVeiculoObject.alterar(veiculoObject);
            }
            
            this.limparCampos();
            this.carregaTable();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void anoFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_anoFieldFocusLost
        try {
            int i = Integer.parseInt(anoField.getText());
        } catch (NumberFormatException e) {
            System.out.println("e");
            anoField.setText("");
        }
    }//GEN-LAST:event_anoFieldFocusLost

    private void veiculosTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_veiculosTableMouseClicked
        codigoField.setText(veiculosTable.getValueAt(veiculosTable.getSelectedRow(), VeiculoTableModel.COL_CODIGO).toString());
        marcaField.setText(veiculosTable.getValueAt(veiculosTable.getSelectedRow(), VeiculoTableModel.COL_MARCA).toString());
        modeloField.setText(veiculosTable.getValueAt(veiculosTable.getSelectedRow(), VeiculoTableModel.COL_MODELO).toString());
        chassiField.setText(veiculosTable.getValueAt(veiculosTable.getSelectedRow(), VeiculoTableModel.COL_CHASSI).toString());
        anoField.setText(veiculosTable.getValueAt(veiculosTable.getSelectedRow(), VeiculoTableModel.COL_ANO).toString());
        btnExcluir.setEnabled(true);
    }//GEN-LAST:event_veiculosTableMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int confirma = JOptionPane.showConfirmDialog(null, "Confirmar Exclusão?", "Exclusão de Veículo", JOptionPane.YES_NO_OPTION);
        if(confirma == 0){
            daoVeiculoObject.excluir(Integer.parseInt(codigoField.getText()));
            this.limparCampos();
            this.carregaTable();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

