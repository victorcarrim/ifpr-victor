    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        String campo;
        
        if(rbtnMarca.isSelected()){
            campo = "marca";
        } else {
            campo = "modelo";
        }
        
        veiculosTable.setModel(new VeiculoTableModel(daoVeiculoObject.listarPorFiltro(campo, filtroField.getText())));
    }//GEN-LAST:event_btnFiltrarActionPerformed

    public void limparCampos(){
        marcaField.setText("");
        modeloField.setText("");
        chassiField.setText("");
        anoField.setText("");
        codigoField.setText("");
}
    public void carregaTable(){
        veiculosTable.setModel(new VeiculoTableModel(daoVeiculoObject.listarTodos()));
        btnExcluir.setEnabled(false);
    }
