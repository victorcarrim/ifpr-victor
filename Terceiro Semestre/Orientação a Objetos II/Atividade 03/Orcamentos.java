public class Orcamentos {
    private String idOrcamento;
    private String produtos;
    private String valorTotoal;
    private String idCliente;
    
    public String getIdOrcamento() {
        return idOrcamento;
    }
    public void setIdOrcamento(String idOrcamento) {
        this.idOrcamento = idOrcamento;
    }
    public String getProdutos() {
        return produtos;
    }
    public void setProdutos(String produtos) {
        this.produtos = produtos;
    }
    public String getValorTotoal() {
        return valorTotoal;
    }
    public void setValorTotoal(String valorTotoal) {
        this.valorTotoal = valorTotoal;
    }
    public String getIdCliente() {
        return idCliente;
    }
    public void setIdCliente(String IdCliente) {
        this.idCliente = IdCliente;
    }

    
}
